fn regular_example() {
    // Example input data: high and low prices
    let (high, low) = expand_inputs();

    let inputs = [high.as_slice(), low.as_slice()];

    // Options for the Fisher Transform calculation: period
    let options = [10.0];

    // Calculate the Fisher Transform using the indicator function
    let (outputs, _) = match indicator(&inputs, &options, None) {
        Ok(result) => result,
        Err(e) => panic!("Error: {}", e),
    };

    println!(
        "Regular Fisher Transform - calculated for {} data points",
        high.len()
    );
    println!(
        "Fisher Line first 5: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}], last: {:.4}",
        outputs[0].get(0).unwrap_or(&0.0),
        outputs[0].get(1).unwrap_or(&0.0),
        outputs[0].get(2).unwrap_or(&0.0),
        outputs[0].get(3).unwrap_or(&0.0),
        outputs[0].get(4).unwrap_or(&0.0),
        outputs[0].last().unwrap_or(&0.0)
    );
    println!(
        "Signal Line first 5: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}], last: {:.4}",
        outputs[1].get(0).unwrap_or(&0.0),
        outputs[1].get(1).unwrap_or(&0.0),
        outputs[1].get(2).unwrap_or(&0.0),
        outputs[1].get(3).unwrap_or(&0.0),
        outputs[1].get(4).unwrap_or(&0.0),
        outputs[1].last().unwrap_or(&0.0)
    );

    let inputs2 = [&high[..high.len() - 5], &low[..low.len() - 5]];

    let (outputs2, mut state2) = match indicator(&inputs2, &options, None) {
        Ok(result) => result,
        Err(e) => panic!("Error: {}", e),
    };
    println!(
        "\nPartial Fisher Transform Line first 5: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}]",
        outputs2[0].get(0).unwrap_or(&0.0),
        outputs2[0].get(1).unwrap_or(&0.0),
        outputs2[0].get(2).unwrap_or(&0.0),
        outputs2[0].get(3).unwrap_or(&0.0),
        outputs2[0].get(4).unwrap_or(&0.0)
    );
    println!(
        "Partial Signal Line first 5: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}]",
        outputs2[1].get(0).unwrap_or(&0.0),
        outputs2[1].get(1).unwrap_or(&0.0),
        outputs2[1].get(2).unwrap_or(&0.0),
        outputs2[1].get(3).unwrap_or(&0.0),
        outputs2[1].get(4).unwrap_or(&0.0)
    );

    // Calculate the Fisher Transform using the stateful batch function
    let new_inputs = [&high[high.len() - 5..], &low[low.len() - 5..]];

    let new_outputs = match state2.batch_indicator(&new_inputs, None) {
        Ok(result) => result,
        Err(e) => panic!("Error: {}", e),
    };
    println!(
        "\nFinal Fisher Transform Line: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}]",
        new_outputs[0].get(0).unwrap_or(&0.0),
        new_outputs[0].get(1).unwrap_or(&0.0),
        new_outputs[0].get(2).unwrap_or(&0.0),
        new_outputs[0].get(3).unwrap_or(&0.0),
        new_outputs[0].get(4).unwrap_or(&0.0)
    );
    println!(
        "Final Signal Line: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}]",
        new_outputs[1].get(0).unwrap_or(&0.0),
        new_outputs[1].get(1).unwrap_or(&0.0),
        new_outputs[1].get(2).unwrap_or(&0.0),
        new_outputs[1].get(3).unwrap_or(&0.0),
        new_outputs[1].get(4).unwrap_or(&0.0)
    );
}

fn simd_by_asset_example() {
    // Create 4 stocks with the same high/low data for easier debugging
    let (high, low) = expand_inputs();

    // Create input structure: array of references to arrays of slices
    // Format: [stock][input_field] where input_fields are [high, low]
    let stock1_inputs = [high.as_slice(), low.as_slice()];
    let stock2_inputs = [high.as_slice(), low.as_slice()];
    let stock3_inputs = [high.as_slice(), low.as_slice()];
    let stock4_inputs = [high.as_slice(), low.as_slice()];

    let inputs: [&[&[f64]; 2]; 4] = [
        &stock1_inputs,
        &stock2_inputs,
        &stock3_inputs,
        &stock4_inputs,
    ];

    // Set options (period = 10)
    let options = [10.0];

    // Calculate Fisher indicator for all 4 stocks in parallel using SIMD
    let (outputs, _states) = match indicator_by_assets::<4>(&inputs, &options, None) {
        Ok(r) => r,
        Err(e) => panic!("Error: {}", e),
    };

    println!(
        "SIMD By Assets - calculated Fisher Transform for {} stocks simultaneously",
        inputs.len()
    );

    // Print first few results for each stock
    for (stock_idx, stock_results) in outputs.iter().enumerate() {
        let fisher_line = &stock_results[0]; // Fisher Transform line
        let signal_line = &stock_results[1]; // Signal line

        println!(
            "Stock {}: Fisher first 5: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}], last: {:.4}",
            stock_idx + 1,
            fisher_line.get(0).unwrap_or(&0.0),
            fisher_line.get(1).unwrap_or(&0.0),
            fisher_line.get(2).unwrap_or(&0.0),
            fisher_line.get(3).unwrap_or(&0.0),
            fisher_line.get(4).unwrap_or(&0.0),
            fisher_line.last().unwrap_or(&0.0)
        );

        println!(
            "Stock {}: Signal first 5: [{:.4}, {:.4}, {:.4}, {:.4}, {:.4}], last: {:.4}",
            stock_idx + 1,
            signal_line.get(0).unwrap_or(&0.0),
            signal_line.get(1).unwrap_or(&0.0),
            signal_line.get(2).unwrap_or(&0.0),
            signal_line.get(3).unwrap_or(&0.0),
            signal_line.get(4).unwrap_or(&0.0),
            signal_line.last().unwrap_or(&0.0)
        );
    }
}

fn simd_by_asset_debug() {
    let (high, low) = expand_inputs();
    // Simple debug case with the same dataset for all stocks
    let stock1_inputs = [high.as_slice(), low.as_slice()];
    let stock2_inputs = [high.as_slice(), low.as_slice()];
    let stock3_inputs = [high.as_slice(), low.as_slice()];
    let stock4_inputs = [high.as_slice(), low.as_slice()];

    let inputs: [&[&[f64]; 2]; 4] = [
        &stock1_inputs,
        &stock2_inputs,
        &stock3_inputs,
        &stock4_inputs,
    ];

    let options = [10.0];

    println!("=== SIMD By Assets Debug ===");
    println!("Testing with period: {}", options[0]);
    println!("Data length: {}", high.len());

    // Calculate regular Fisher for comparison
    let regular_inputs = [high.as_slice(), low.as_slice()];
    let (regular_outputs, _) = match indicator(&regular_inputs, &options, None) {
        Ok(r) => r,
        Err(e) => panic!("Regular Error: {}", e),
    };

    // Calculate SIMD Fisher
    let (simd_outputs, _) = match indicator_by_assets::<4>(&inputs, &options, None) {
        Ok(r) => r,
        Err(e) => panic!("SIMD Error: {}", e),
    };

    println!("\n=== Results Comparison ===");

    // Check if they match (comparing first stock from SIMD with regular)
    let fisher_match = regular_outputs[0]
        .iter()
        .zip(simd_outputs[0][0].iter())
        .all(|(r, s)| (r - s).abs() < 1e-10);

    let signal_match = regular_outputs[1]
        .iter()
        .zip(simd_outputs[0][1].iter())
        .all(|(r, s)| (r - s).abs() < 1e-10);

    if fisher_match && signal_match {
        println!("\n✓ SIMD and regular results match perfectly!");
    } else {
        println!("\n❌ SIMD and regular results do NOT match!");

        // Print detailed differences for Fisher line
        println!("\nFisher Transform differences:");
        for (i, (r, s)) in regular_outputs[0]
            .iter()
            .zip(simd_outputs[0][0].iter())
            .enumerate()
        {
            if (r - s).abs() >= 1e-10 {
                println!(
                    "  Index {}: Regular={:.10}, SIMD={:.10}, Diff={:.10}",
                    i,
                    r,
                    s,
                    r - s
                );
                if i > 10 {
                    break;
                } // Limit output
            }
        }

        // Print detailed differences for Signal line
        println!("\nSignal Line differences:");
        for (i, (r, s)) in regular_outputs[1]
            .iter()
            .zip(simd_outputs[0][1].iter())
            .enumerate()
        {
            if (r - s).abs() >= 1e-10 {
                println!(
                    "  Index {}: Regular={:.10}, SIMD={:.10}, Diff={:.10}",
                    i,
                    r,
                    s,
                    r - s
                );
                if i > 10 {
                    break;
                } // Limit output
            }
        }
    }

    // Print some sample values for verification
    println!("\nSample Results:");
    println!(
        "Regular Fisher: {:?}",
        &regular_outputs[0][..10.min(regular_outputs[0].len())]
    );
    println!(
        "SIMD Fisher: {:?}",
        &simd_outputs[0][0][..10.min(simd_outputs[0][0].len())]
    );
    println!(
        "Regular Signal: {:?}",
        &regular_outputs[1][..10.min(regular_outputs[1].len())]
    );
    println!(
        "SIMD Signal: {:?}",
        &simd_outputs[0][1][..10.min(simd_outputs[0][1].len())]
    );
}

